#!/usr/bin/env python3
"""
Unit tests for init_project.py

Tests the deterministic scaffold generation. Note: long-task-guide.md and
init.sh/init.ps1 are NOT generated by init_project.py — they are created
by the LLM during the Initializer phase. See test_validate_guide.py for
guide validation tests.
"""

import json
import os
import shutil
import subprocess
import sys
import tempfile

SCRIPT_PATH = os.path.join(os.path.dirname(__file__), "..", "skills", "long-task-init", "scripts", "init_project.py")


def run_init(project_name, output_dir, extra_args=None):
    """Run init_project.py, return (exit_code, stdout, stderr)."""
    cmd = [sys.executable, SCRIPT_PATH, project_name, "--path", output_dir]
    if extra_args:
        cmd.extend(extra_args)
    result = subprocess.run(cmd, capture_output=True, text=True)
    return result.returncode, result.stdout, result.stderr


def test_creates_all_artifacts():
    """init_project.py should create all expected files and directories."""
    tmp = tempfile.mkdtemp()
    try:
        code, stdout, _ = run_init("test-project", tmp)
        assert code == 0, f"Expected exit 0, got {code}"

        expected_files = [
            "feature-list.json",
            "CLAUDE.md",
            "AGENTS.md",
            "task-progress.md",
            "RELEASE_NOTES.md",
            os.path.join("examples", "README.md"),
        ]
        for f in expected_files:
            path = os.path.join(tmp, f)
            assert os.path.exists(path), f"Missing: {f}"

        expected_dirs = [
            "scripts",
            "examples",
            os.path.join("docs", "plans"),
        ]
        for d in expected_dirs:
            path = os.path.join(tmp, d)
            assert os.path.isdir(path), f"Missing directory: {d}"
    finally:
        shutil.rmtree(tmp)


def test_does_not_create_guide_or_init_scripts():
    """init_project.py should NOT create LLM-generated files (guide, init scripts)."""
